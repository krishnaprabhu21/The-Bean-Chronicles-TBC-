export function getDailyEntry() {
  const now = new Date()
  const start = new Date(now.getFullYear(), 0, 0)
  const dayOfYear = Math.floor((now - start) / (1000 * 60 * 60 * 24))
  return dailyCoffeeEntries[dayOfYear % dailyCoffeeEntries.length]
}
